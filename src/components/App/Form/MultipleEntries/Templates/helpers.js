import * as Yup from "yup";

const isMainSection = (currentSectionName) =>
  !!(currentSectionName === "Career" || currentSectionName === "Education");

const getFormikData = (
  currentSectionName,
  entryBeingEdited,
  isCurrentlyEditing
) => {
  const initialValues = () => {
    return isMainSection(currentSectionName)
      ? {
          status: isCurrentlyEditing ? entryBeingEdited.status : false,
          establishment: isCurrentlyEditing
            ? entryBeingEdited.establishment
            : "",
          context: isCurrentlyEditing ? entryBeingEdited.context : "",
          startDate: isCurrentlyEditing ? entryBeingEdited.startDate : "",
          endDate: isCurrentlyEditing ? entryBeingEdited.endDate : "",
          description: isCurrentlyEditing ? entryBeingEdited.description : "",
        }
      : {
          extra: isCurrentlyEditing ? entryBeingEdited.status : false,
          proficiency: isCurrentlyEditing ? entryBeingEdited.proficiency : "",
        };
  };

  const validationSchema = () => {
    return isMainSection(currentSectionName)
      ? Yup.object({
          status: Yup.boolean(),
          establishment: Yup.string().required(
            `Please enter the name of the ${
              currentSectionName === "Career" ? "company" : "school"
            }`
          ),
          context: Yup.string().required(
            `Please enter your ${
              currentSectionName === "Career" ? "position" : "field"
            }`
          ),
          startDate: Yup.date().required("Please enter your starting date"),
          endDate: Yup.date(),
          description: Yup.string()
            .required("Please enter a description")
            .max(50),
        })
      : Yup.object({
          extra: Yup.string().required(
            `Please enter a ${
              (currentSectionName === "Hobbies" && "hobby") ||
              (currentSectionName === "Languages" && "language") ||
              (currentSectionName === "Tools" && "tools")
            }`
          ),
          proficiency: Yup.string().required("Please enter your skill level"),
        });
  };

  return {
    initialValues: initialValues(),
    validationSchema: validationSchema(),
  };
};

